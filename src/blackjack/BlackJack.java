    private void beregnProsent(){
        if(kontroll.hentAntallKort() == 0){
            lblBildekortProsent.setText("(0 %)");
            lblEssProsent.setText("(0 %)");
        } else {
            DecimalFormat decimalformat = new DecimalFormat("#.##");
            double antallkort = (double)kontroll.hentAntallKort();
            double antalless = (double)kontroll.essigjen;
            double antallbildekort = (double)kontroll.bildekortigjen;
            double prosentess = antalless/antallkort*100;
            double prosentbildekort = antallbildekort/antallkort*100;
            lblBildekortProsent.setText("("+String.valueOf(decimalformat.format(prosentbildekort))+" %)");
            lblEssProsent.setText("("+String.valueOf(decimalformat.format(prosentess))+" %)");
        }
    }
    
