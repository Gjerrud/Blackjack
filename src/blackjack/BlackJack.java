    private void spillerMax(){
        if(kontroll.sumspiller > 21 && kontroll.essspiller > 0){
            kontroll.sumspiller = kontroll.sumspiller - 10;
            lblSpillerSum.setText(String.valueOf(kontroll.sumspiller));
            kontroll.essspiller--;
            spillerMax();
        }
        if(kontroll.sumspiller > 20){
            btnTrekk.setEnabled(false);
            btnDouble.setEnabled(false);
        }
    }
    
    private void spillerMaxSplit(){
        if(kontroll.splitverdi > 21 && kontroll.essspiller > 0){
            kontroll.splitverdi = kontroll.splitverdi - 10;
            lblSpillerSumSplit.setText(String.valueOf(kontroll.splitverdi));
            kontroll.essspiller--;
            spillerMax();
        }
        if(kontroll.splitverdi > 20){
            btnTrekkSplit.setEnabled(false);
        }
    }
    
    private boolean dealerMax(){
        if(kontroll.sumspiller > 21){
            return false;
        } else {
            lblDealer2.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Min_Kort/"+kontroll.dealerkort+".png")));
            lblDealerSum.setText(String.valueOf(kontroll.sumdealer));
        }
        if(kontroll.sumdealer > 21 && kontroll.essdealer > 0){
            kontroll.sumdealer = kontroll.sumdealer - 10;
            lblDealerSum.setText(String.valueOf(kontroll.sumdealer));
            kontroll.essdealer--;
            dealerMax();
        }
        if(kontroll.sumdealer < 17){
            return true;
        } else {
            return false;
        }
    }
    
    private void resultat(){
        int bet = Integer.parseInt(cmbStart.getSelectedItem().toString());
        if(kontroll.sumdealer == kontroll.sumspiller){
            if(kontroll.spillersplit){
                JOptionPane.showMessageDialog(null, "Uavgjort p√• f√∏rste h√•nden");
            } else {
                JOptionPane.showMessageDialog(null, "Uavgjort");
            }
            if(kontroll.spillerdouble == false){
                kontroll.penger += bet;
            } else {
                kontroll.penger += bet*2;
            }
            kontroll.runderspilt += 1;
            lblChips.setText("Chips: "+String.valueOf(kontroll.penger));
        } else if(kontroll.sumdealer <= 21 && kontroll.sumdealer > kontroll.sumspiller){
            if(kontroll.spillersplit){
                JOptionPane.showMessageDialog(null, "Dealer vant f√∏rste h√•nden");
            } else {
                JOptionPane.showMessageDialog(null, "Dealer vant");
            }
            kontroll.runderspilt += 1;
        } else if(kontroll.sumdealer <= 21 && kontroll.sumspiller > 21){
            if(kontroll.spillersplit){
                JOptionPane.showMessageDialog(null, "Dealer vant f√∏rste h√•nden");
            } else {
                JOptionPane.showMessageDialog(null, "Dealer vant");
            }
            kontroll.runderspilt += 1;
        } else {
            if(kontroll.spillersplit){
                JOptionPane.showMessageDialog(null, "Du vant f√∏rste h√•nden");
            } else {
                JOptionPane.showMessageDialog(null, "Du vant");
            }
            if(kontroll.spillerdouble == false){
                kontroll.penger += bet*2;
            } else {
                kontroll.penger += bet*4;
            }
            kontroll.runderspilt += 1;
            lblChips.setText("Chips: "+String.valueOf(kontroll.penger));
        }
    }
    
